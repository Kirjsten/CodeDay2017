    private void okButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_okButtonMouseClicked
        //read from upcText
        upc = Integer.parseInt(upcText.getText());
        int upcGet;
        boolean found = false;
        for(int i = 0; i < upcList.size(); i++)
        { 
            upcGet = upcList.get(i).getUPC();
            if(upcGet==upc)
            {
                //change panel to display upc information
                homePage.setVisible(false);
                cpuCodeLabel.setText(Integer.toString(upcList.get(i).getUPC()));
                itemLabel.setText(upcList.get(i).getItem());
                manufactLabel.setText(upcList.get(i).getManufact());
                
                int recycle = upcList.get(i).getRecycleType();
                switch(recycle)
                {
                    case 1:     
                    case 2:
                    case 3:
                    case 4:
                    case 5:
                    case 6:
                        recycleTypeLabel.setText("A plastic recyclable.");
                        break;
                    case 8:
                        recycleTypeLabel.setText("A metal recycleable");
                        break;
                    case 9:
                        recycleTypeLabel.setText("A paper or cardboard recycable");
                        break;
                    case 10:
                        recycleTypeLabel.setText("A glass recyclable");
                        break;
                    case 11:
                        recycleTypeLabel.setText("A battery or bulb recyclable. Special Recycle Needed");
                        break;
                    case 12:
                        recycleTypeLabel.setText("An electronic recyclable product.Special Recycle Needed");
                        break;
                        
                            default:
                                recycleTypeLabel.setText("Not Recyclable");
                                break;
                }
                displayInfo.setVisible(true);
                found = true;
                
            }
        }       
        
        if(!found)
        {
            //change panel to create up
            homePage.setVisible(false);
            displayEdit.setVisible(true);
        }
       
    }//GEN-LAST:event_okButtonMouseClicked

